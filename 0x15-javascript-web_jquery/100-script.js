// Function to change the text color of the <header> element to red (#FF0000)
function updateHeaderColor() {
    // Select the <header> element using document.querySelector
    const headerElement = document.querySelector('header');
    
    // Update the text color of the <header> element to red (#FF0000)
    headerElement.style.color = '#FF0000';
}

// Call the function when the script is loaded
updateHeaderColor();
